package com.homework.q11helper;

public class Helper {

	private float n1;
	private float n2;
	
	public Helper(float n1, float n2) {
		super();
		this.n1 = n1;
		this.n2 = n2;
	}

	public float getN1() {
		return n1;
	}

	public void setN1(float n1) {
		this.n1 = n1;
	}

	public float getN2() {
		return n2;
	}

	public void setN2(float n2) {
		this.n2 = n2;
	}
	
	
	
}
